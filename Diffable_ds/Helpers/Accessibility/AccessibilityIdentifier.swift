enum AccessibilityIdentifiers {

    enum ListView {
        static let addButton = "AddButton"
        static let deleteButton = "DeleteButton"
        static let textField = "TextField"
        static let collectionView = "CollectionView"
    }
    
    enum ListViewSection {
        static let sectionTitleLabel = "SectionTitleLabel"
    }
    
    enum ListViewCell {
        static let titleLabel = "TitleLabel"
    }
    
}
